namespace WinLoadingDialog.Controls;

public enum SpinnerMode
{
    /// <summary>
    /// Rotating arc with a soft tail.
    /// </summary>
    Arc,

    /// <summary>
    /// Classic segmented circular spinner.
    /// </summary>
    Circle,

    /// <summary>
    /// Circular progress-style spinner with a track.
    /// </summary>
    CircularProgress,

    /// <summary>
    /// Animated horizontal dots.
    /// </summary>
    Dots,

    /// <summary>
    /// Pulsing circle animation.
    /// </summary>
    Pulse,

    /// <summary>
    /// Rotating ring with a gap.
    /// </summary>
    Ring,

    /// <summary>
    /// Animated vertical bars.
    /// </summary>
    Bars,

    /// <summary>
    /// Two rotating rings moving in opposite directions.
    /// </summary>
    DualRing
}